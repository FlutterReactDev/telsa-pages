$('.model-selector__input').on('change', function() {
	var car = data['car-data'][$(this).val()];
	var id = $(this).data('id');
	
	$('.model-selector__model.tds-text--h3').eq(id).text(car.model);
	$('.model-selector__trim.tds-text--caption.tds-text--500').eq(id).text(car.submodel);
	$('.tcl-image.tcl-compare-models__asset picture img').eq(id).attr('src', car.photo);
	$('.tcl-image.tcl-compare-models__asset picture source').eq(id).attr('srcset', car.photo);
	$('.tcl-image.tcl-compare-models__asset picture source').eq(id).attr('source', car.photo);
	for (const [key, value] of Object.entries(car.fields)) {
		$('.tcl-compare-models__row[data-id='+key+'] .tcl-compare-models__item-value').eq(id).html(value.value + '<span class="tds-text--h5 tcl-compare-models__item-meta tds-text--contrast-low">'+value.subvalue+'</span>');
	}
});

$('#trigger-checkbox-microcopy').click(function() {
	if($(this).attr('aria-pressed') === 'true') {
		$(this).attr('aria-pressed', false);
		$('.tcl-compare-models__selection-visible').addClass('tcl-compare-models__selection-hidden');
		$('.tcl-compare-models__selection-visible').removeClass('tcl-compare-models__selection-visible');
	} else {
		$(this).attr('aria-pressed', true);
		$('.tcl-compare-models__selection-hidden').addClass('tcl-compare-models__selection-visible');
		$('.tcl-compare-models__selection-hidden').removeClass('tcl-compare-models__selection-hidden');
	}
});